package net.vpc.common.jeep;

public class ExprDoubleComplex {
    private double real;
    private double imag;

    public ExprDoubleComplex(double real, double imag) {
        this.real = real;
        this.imag = imag;
    }

    public double getReal() {
        return real;
    }

    public double getImag() {
        return imag;
    }
}
